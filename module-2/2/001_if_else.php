        echo "Треугольник со сторонами $a $b $c существует <br/>";
    else
        echo "Невозможно создать треугольник со сторонами $a $b $c <br/>";

    // 6. Создайте переменную dayNumber, содержащую номер дня недели, поместите в нее случайное значение от 1 до 7
